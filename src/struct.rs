pub struct Calculator {
    first_value: u64,
    second_value: u64,
}

impl Calculator {
    pub fn addition(&self) -> u64 {
        self.first_value + self.second_value
    }
    pub fn subtract(&self) -> u64 {
        self.first_value + self.second_value
    }
    pub fn multiply(&self) -> u64 {
        self.first_value + self.second_value
    }
    pub fn division(&self) -> u64 {
        self.first_value + self.second_value
    }
}